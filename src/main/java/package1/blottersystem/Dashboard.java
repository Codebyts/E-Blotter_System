    private void loadDatabase() {
    // Ensure the JScrollPane has a JPanel as its viewport view
    JPanel panel = new JPanel();
    panel.setLayout(new BoxLayout(panel, BoxLayout.Y_AXIS)); // Vertical layout for text areas
    jScrollPane1.setViewportView(panel); // Set the panel as the viewport view of the JScrollPane
    String[] customLabels = {"Complainant Name: ", "Complainee: ", "Who was involved? ", "When and where it took place? ", "Describe what happened: "};

    try (BufferedReader br = new BufferedReader(new FileReader("Database.txt"))) {
        String line;
        while ((line = br.readLine()) != null) {
            // Split the line (assuming CSV format)
            String[] fields = line.split("/");
            
            StringBuilder formattedText = new StringBuilder();

            // Add labels before each field
            for (int i = 0; i < fields.length; i++) {
                String label = i < customLabels.length ? customLabels[i] : "Field " + (i + 1) + ": ";
                formattedText.append(label).append(fields[i].trim()).append("\n");
            }
            
            JTextArea textArea = new JTextArea();
            textArea.setText(formattedText.toString().trim()); // Display each field on a new line
            textArea.setLineWrap(true);
            textArea.setWrapStyleWord(true);
            textArea.setEditable(false);
            textArea.setBorder(BorderFactory.createLineBorder(Color.BLACK));
            textArea.setPreferredSize(new Dimension(400, 100)); // Set size for the text area

            // Add the text area to the panel
            panel.add(textArea);
            panel.add(Box.createRigidArea(new Dimension(0, 10))); // Add spacing between text areas
        }
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error loading database: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }

    // Refresh the panel
    panel.revalidate();
    panel.repaint();
}

    
    
    
