    public void fileWriter() {
        try (FileWriter writer = new FileWriter("Database.txt", true)) { // true appends to the file
            // Retrieve the text from the text field and write it to the file
            String complainantName = Complainant_Name.getText();
            String complainee = Complainee.getText();
            String Whowasinvolved = Who_was_involved.getText();
            String WhenAndWhereItTookPlace = When_and_where_it_took_place.getText();
            String DescribeWhatHappened = Describe_what_happened.getText();
            if (complainantName.isEmpty() || complainee.isEmpty() || Whowasinvolved.isEmpty() || WhenAndWhereItTookPlace.isEmpty() || DescribeWhatHappened.isEmpty()) {
                JOptionPane.showMessageDialog(null, "The name field cannot be empty!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
                writer.write(complainantName + "/" +  complainee + "/" + Whowasinvolved + "/"
                + WhenAndWhereItTookPlace + "/" + DescribeWhatHappened + System.lineSeparator());
                
                loadDatabase();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "An error occurred while saving your response: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
        
