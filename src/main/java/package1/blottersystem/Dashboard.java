    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        String complainantNames = complainantName.getText(); // Get the text from complainant name
        String complainees = complainee.getText(); // Get the text from complainee name
        String whoWasInvolve = whoWasInvolved.getText(); // Get the text from involved person

        String filePath = "Database.txt"; // Path to the database file

        boolean reportFound = false; // Flag to indicate if the report is found

        try (BufferedReader reader = new BufferedReader(new FileReader(filePath))) {
            String line;

            // Read through each line in the file
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split("/");
                // Match each part of the line separately
                if (parts.length >= 3) {
                    String fileComplainant = parts[0].trim();
                    String fileComplainee = parts[1].trim();
                    String fileInvolved = parts[2].trim();

                    // Check if the extracted parts match the user input
                    if (fileComplainant.equalsIgnoreCase(complainantNames) &&
                        fileComplainee.equalsIgnoreCase(complainees) &&
                        fileInvolved.equalsIgnoreCase(whoWasInvolve)) {
                        reportFound = true;
                        break;
                    }
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Error reading the file: " + e.getMessage(),
                "Error", JOptionPane.ERROR_MESSAGE);
        }

        if (reportFound) {
            JOptionPane.showMessageDialog(null, "Report Found!");
            jTabbedPane1.setSelectedIndex(4); 
        } else {
            JOptionPane.showMessageDialog(null, "Report Not Found.");
            jTabbedPane1.setSelectedIndex(2); // Go to tab 2 (dashboard)

        }
    }//GEN-LAST:event_submitActionPerformed

